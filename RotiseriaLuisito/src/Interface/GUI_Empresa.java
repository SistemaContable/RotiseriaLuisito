    private boolean existeRazonSocial (String name){
        boolean existe = false;
        try {
            ResultSet rsl = r_con.Consultar("SELECT * FROM Empresas WHERE razon_social = '"+name+"'");
            existe = (rsl.next());
        } catch (SQLException ex) {
            Logger.getLogger(GUI_Empresa.class.getName()).log(Level.SEVERE, null, ex);
        }
        return existe;
    }
