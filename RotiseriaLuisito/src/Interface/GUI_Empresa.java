    private boolean validarCampos (){
        boolean validos = true;
        
        if (fieldRazonSocial.getText().equals("")){
            fieldRazonSocial.requestFocus();
            msj_Error("Ingrese una Razon Social.");
            validos=false;
        }
        else{
            if (!control_f .isFechaValida(fieldFechaInicio.getText())){
                msj_Error("La Fecha de Apertura no es valida.");
                fieldFechaInicio.requestFocus();
                validos=false;
            } 
            else{
                if (!control_f .isFechaValida(fieldFechaFin.getText())){
                    msj_Error("La Fecha de Cierre no es valida.");
                    fieldFechaFin.requestFocus();
                    validos=false;
                }
                else{
                    if (control_f.menorFechas(fieldFechaInicio.getText(), fieldFechaFin.getText())==2){
                            msj_Error("La Fecha de Apertura es mayor a la de Cierre.");
                            fieldFechaInicio.requestFocus();
                            validos=false;
                    }                     
                    else{
                        if (fieldUsuario.getText().equals("")){
                            msj_Error("Ingrese un Usuario Administrador.");
                            fieldUsuario.requestFocus();
                            validos=false;
                        }
                        else{
                            this.vaciarMensaje();
                        }
                    }
                }
            }
        }        
        return validos;
    }

